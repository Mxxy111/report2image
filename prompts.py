_PATIENT_FRIENDLY_VISUALIZATION_RULES = """
【任务目标】
根据本次 PET-CT 报告生成一张患者友好的医学信息图。目标是帮助患者快速理解“哪里有发现、发现的性质倾向、FDG 摄取情况及 SUVmax”，不是复刻 PET/CT 影像，也不是绘制完整人体解剖图谱。

【事实边界】
1. 报告原文是唯一医学事实来源。不得增加报告未提及的病灶、器官异常、数值、诊断或结论。
2. 不得根据常识、颜色、SUVmax 或既往病例自行推断良恶性；必须保留报告中的确定程度，如“考虑”“可能”“可疑”“未见明显异常”。
3. 报告未给出病灶大小、SUVmax 或性质时，不得补写或估算。
4. 如果检查所见与检查结论存在表述差异，优先保留原文中的限定语，不自行消除不确定性。

【生成前事实清单】
1. 先在内部提取一张结构化发现清单；每一项至少包含：原文短句、解剖部位、侧别、性质倾向、FDG/SUVmax、应使用颜色、是否需要绘制。
2. 清单中的每一项都必须在成图前后逐项核对：不能合并到错误器官，不能把一个病灶的 SUVmax 或颜色挪给另一项发现。
3. 对“未见 FDG 代谢增高”“请结合内镜/随访”“考虑炎症/囊肿/术后改变”等低风险或不确定发现，必须在清单中保留原始限定语，不得升级为恶性。

【患者友好视觉风格】
1. 纯白背景，二维扁平医学简笔画；整体温和、清楚、克制，避免过度医学化和压迫感。
2. 使用深灰黑色轮廓、浅灰蓝色器官和少量病灶强调色。不要使用真实 PET/CT 切片、热力图、写实人体、3D 解剖、血腥细节、医院仪器界面或装饰性背景。
3. 人体正面轮廓位于画面中央，标签分布在两侧，保留充足留白。不要添加标题栏、图例框、卡片、编号列表、边框、水印或无关图标。
4. 人体轮廓应完整、自然、比例稳定；默认显示从头部到大腿上段。只有报告涉及膝以下病变时才扩展至全身。

【解剖结构与器官选择】
1. 只绘制报告明确涉及的器官、组织和为定位病变所必需的少量解剖标志；不得为了“看起来完整”而画出全部内脏。
2. 脊柱、骨盆等定位结构仅在有助于定位时简化显示。报告涉及成对器官时，应画出双侧轮廓用于定位，但只标注有报告依据的发现。
3. 器官形态、相对位置、层次关系和病灶所在区域必须符合基础解剖。不要让器官互相覆盖到不合理的位置。
4. 报告明确写明器官切除、术后缺如时，可用浅灰色虚线轮廓表示；未明确写明时不得推断器官缺失。
5. 淋巴结发现必须画成淋巴结/结节点，不能画在胃、肾、肠腔或其他实体器官内部；引导线端点必须落在相应淋巴结区域。
6. 腹膜后淋巴结位于腹腔后方、主动脉/下腔静脉旁区域；髂血管旁淋巴结位于骨盆髂血管走行附近；肠系膜淋巴结位于肠管系膜区域。不要把这些区域互相替换。
7. 乳腺、肺叶、肾上腺、肾脏、骨骼等成对结构必须按报告侧别放置；例如左侧乳腺在患者左胸，右肺上叶在患者右肺上部。
8. 淋巴结、骨病灶或多发病灶应位于报告所述解剖分区，不得随意散布。

【左右侧别：仅用于内部定位核对】
1. 固定采用正面面对患者的视角。先确定人体中线，再应用唯一映射：患者右侧位于画面左侧，患者左侧位于画面右侧。不得按观察者自身左右习惯放置。
2. 用成对器官做内部校准：患者右肺、右肾、右乳腺应位于画面左侧；患者左肺、左肾、左乳腺应位于画面右侧。输尿管、肾上腺、肺叶、甲状腺叶、髂血管旁区域及四肢同样服从此映射。
3. 头部、胸腹部和骨盆必须保持同一正面朝向，不得出现侧面头部搭配正面躯干、局部镜像、左右器官互换或方向不明的混合视角。
4. 生成前必须在内部建立“内部侧别核对表”，逐项记录：报告原文侧别、患者侧别、预期画面侧、对应器官/区域、病灶点预期位置。报告写“双侧”时必须核对两边；未写侧别时不得擅自补充。
5. 生成后必须沿同一张表逐项复核。侧别以器官、病灶圆点及引导线端点相对人体中线的实际位置为准；标签放在画面哪一边与病灶侧别无关，不能用标签排版位置代替解剖定位。
6. 标签文字与实际落点必须同时正确。若文字写“左侧”但病灶点落在画面左侧，仍然是错误；应移动病灶点/引导线端点到画面右侧，而不是把文字改成“右侧”。
7. 对膀胱壁、肺叶、甲状腺叶、乳腺、肾脏、输尿管、肾上腺、淋巴结分区等侧别发现，必须让引导线端点落在对应患者侧的结构内或紧邻正确区域，不能落在中线、对侧器官或无法判断侧别的位置。
8. 此规则只用于内部定位与核对。成图中不要出现 R、L、“镜像”“图片左侧/右侧”或左右映射说明。

【病变性质与颜色】
1. 鲜红色（#E63946）：仅用于报告明确描述为恶性、转移、复发，或明确高度怀疑恶性的发现；“M可能”“转移可能”“考虑转移”“疑似转移”归入这一类，但必须保留“不确定/可能”的文字。
2. 深蓝灰色（#405E6B）：用于报告明确描述为良性、炎症、囊肿、增生、陈旧性改变或其他低风险发现。
3. 中性灰色（#7B858B）：用于性质未定、仅描述代谢改变但未给出良恶性倾向的发现。必须在文字中保留“不确定/考虑/可能”等原意。
4. 不得仅因 FDG 摄取增高或 SUVmax 较高就把病变画成恶性红色；炎症也可能高摄取，性质必须服从报告结论。
5. 病灶形状和相对大小可帮助定位，但不得把示意图大小当作精确测量。报告给出尺寸时，在标签中保留原值。
6. 每个病灶圆点、引导线端点和标签文字必须使用同一项发现的颜色；不得出现红色圆点连接到良性/炎症标签，或蓝灰色圆点连接到转移标签。
7. 同一区域有多种性质时，必须拆成多条标签或用清晰分组，不能用一种颜色概括所有发现。

【FDG 与 SUVmax】
1. 报告给出 SUVmax 时，必须原样、准确地写入对应病灶标签，例如“SUVmax 8.6”；不得省略、改写、四舍五入或移到错误病灶。
2. 报告只写“代谢增高/轻度增高/未见增高”时，只保留相应定性描述，不得生成数值。
3. 多发病灶拥挤时，可以按同一解剖区域合并标签，但不得丢失关键病灶的 SUVmax、尺寸、侧别和性质倾向，也不得创造报告中没有的范围或最大值。
4. 同一病例出现多个 SUVmax 时，应让患者能看出数值高低差异：优先保留精确数值，可用标签顺序、轻微点大小或“较高/较低”文字辅助区分；但 SUVmax 高低只表示摄取强弱，不能改变良恶性颜色分类。

【中文标注与信息层级】
1. 所有患者可见文字使用准确、完整、可辨认的简体中文；PET/CT、FDG、SUVmax 等报告原有缩写可以保留。
2. 优先采用患者容易理解的短句，同时保持医学含义不变。例如可将长句压缩为“右肺上叶结节（2.3 cm），FDG 代谢增高，考虑转移”。
3. 不得出现乱码、错别字、残缺字、拼音、无意义字符或英文长句。
4. 每条标签用一条清晰的黑色直线指向对应器官或病灶。引导线不得指错、交叉、穿过大段文字或与其他标签混淆。
5. 标签按解剖区域分组，左右两侧整齐排布。关键恶性/转移发现优先，其次为重要良性发现和与本次诊疗相关的阴性结论。
6. 不要机械抄录整份报告；只保留帮助患者理解本次疾病状态的关键内容。不得因版面拥挤而删除关键病灶、SUVmax、侧别或性质倾向。

【生成前内部核对】
- 是否已经完成结构化发现清单，并逐项映射到画面中的病灶、标签、颜色和引导线？
- 报告中的每个关键病灶是否都已呈现，且没有新增病灶？
- 器官形态、解剖位置、病灶位置和左右侧别是否准确？
- 是否只绘制了报告相关器官和必要定位结构？
- 颜色是否严格服从报告的良恶性/炎症等性质描述，而非自行推断？
- SUVmax、尺寸和 FDG 描述是否逐项对应且数值准确？
- 中文是否完整正确，标签与引导线是否一一对应？
- 整体是否像患者教育信息图，而不是专业影像诊断图或完整解剖图谱？
"""


